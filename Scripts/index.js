let categoryVariable = "general";
const starredArticle = document.querySelector(".main_star_news");
const headlineGrid= document.querySelector(".headlines_grid");


// Method to create article element with .art class
const createArticle = (containerElement) =>{
    let articleNode = document.createElement("article");
    let articleImageNode = document.createElement("div");
    let articleDataNode = document.createElement("div");
    articleImageNode.classList.add("art_img_container");
    articleDataNode.classList.add("art_data_container");
    articleNode.classList.add("art");
    articleNode.appendChild(articleDataNode);
    articleNode.appendChild(articleImageNode);
    containerElement.appendChild(articleNode);
}
const createImage = (containerElement, infoImageURL)=>{
    let imageUrlNode = document.createElement("img");
    imageUrlNode.classList.add("art_img");
    imageUrlNode.setAttribute("src", infoImageURL);
    containerElement.appendChild(imageUrlNode);
}
const setImage = (infoImageURL)=>{
    imageUrlNode.setAttribute("src", infoImageURL);
}
// Method to create article title
const createTitle = (containerElement, infoTitle) => {
    let titleNode = document.createElement("h2");
    titleNode.classList.add("art_title");
    titleNode.innerText = infoTitle;
    containerElement.appendChild(titleNode);
}

// method to create article description
const createDescription = (containerElement, infoDescription) => {
    let descriptionNode = document.createElement("p");
    descriptionNode.classList.add("art_description");
    descriptionNode.innerText = infoDescription;
    containerElement.appendChild(descriptionNode);
}
// method to create article snippet
const createSnippet = (containerElement, infoSnippet) => {
    let snippetNode = document.createElement("p");
    snippetNode.classList.add("art_snippet");
    snippetNode.innerText = infoSnippet;
    containerElement.appendChild(snippetNode);
}
// Method to create link with .linkButton class
const createLink = (containerElement, infoURL) => {
    let urlNode = document.createElement("a");
    urlNode.setAttribute('href', infoURL);
    urlNode.setAttribute('target', "_blank")
    urlNode.classList.add("art_link_button")
    urlNode.innerText = "Learn More";
    containerElement.appendChild(urlNode);
}
// Method to fill the grid with articles
const fillGrid = (headlineGrid) => {
    for (let index = 1; index <= 9; index++) {
        createArticle(headlineGrid);
    }
}
// Method to fill the articles with data
const fillArticle= ({
    title,
    description,
    keywords,
    snippet,
    url,
    image_url,
    publishDate,
    categories,
}, articleElement) =>{
    articleElement.childNodes[0].textContent = '';
    articleElement.childNodes[1].textContent = '';
    createImage(articleElement.childNodes[1], image_url);
    createTitle(articleElement.childNodes[0], title);
    createDescription(articleElement.childNodes[0], description);
    createSnippet(articleElement.childNodes[0], snippet)
    createLink(articleElement.childNodes[0],url);
}
// Method to fill the grid articles
const fillArticleGrid = (headlineGrid, nodesListData,page) =>{
    let articleList = headlineGrid.childNodes;
    console.log(articleList);
    if (page == "1") {
        for (let index = 1; index < 5; index++) {
            const gridElement = articleList[index];
            fillArticle(nodesListData[index],gridElement);
        }
    }else if(page == "2"){
        console.log("fillArticleGrid page 2")
        for (let index = 5; index < 10; index++) {
            const gridElement = articleList[index];
            fillArticle(nodesListData[index-5],gridElement);
        }
    }
};
